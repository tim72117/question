				<a id="Q3"><h3>問卷填答錯誤，可否修改？</h3></a>
				
				<p>填答答案一旦按下一頁後就無法再修改。若您欲修改已填答完畢之答案，可逕行與本中心聯絡，電話：(02)7734-3669、E-mail：tes@deps.ntnu.edu.tw</p>
    
				
				<div style="border-top:1px solid #ddd;margin:50px 0 50px 0"></div>
				<a id="Q4"><h3>問卷沒填完，卻需要離開，怎麼辦？</h3></a>				
				<p>若必須離開，請填寫完一頁後按【下一頁】儲存，再次登入後可由上次離開處繼續填答。</p>
				
				
				
				
