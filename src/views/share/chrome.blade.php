				<a id="Q1"><h3>使用Google Chrome瀏覽器</h3></a>
							
				<p>
					下拉式選單無法向下拉動時，請依照下列步驟修改瀏覽器設定
				</p>
				
				<p>
					STEP1：點選開始功能表，找到Google Chrome目錄下的執行檔Google Chrome點選右鍵，在選單最下方點選內容。
				</p>
