				<a id="Q2"><h3>無法登入問卷，或點選下一頁無回應。</h3></a>
				<p>為確保您能夠順利填答問卷，請在瀏覽器網頁上設定為：
					<br />
					<font color=#ff3333><b>[工具]-&gt;[網際網路選項]-&gt;[隱私權]</b></font>，將設定調整為<font color=#ff3333>[中]</font>或<font color=#ff3333>[中高]</font>。若設定為封鎖 cookie 或 Javascript 則無法進行登入。</p>
				<p>若參照1.的方法還是無法登入，請在瀏覽器網頁上設定為：
					<br />
					<font color=#ff3333><b>[工具]-&gt;[網際網路選項]-&gt;[隱私權]-&gt;[進階]</b></font>，勾選<font color=#ff3333>覆寫自動Cookie處理</font>，<font color=#ff3333>第一方Cookie與第三方Cookie</font>都選擇接受，並勾選<font color=#ff3333>自動允許工作階段Cookie</font>。</p>
    
				
				
